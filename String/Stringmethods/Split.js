/*
=>split is used to split the given string in to the array.
=>it is used to split the given string based on the character.
=>if no character is provided then the by default splitting operation will take place.
=>if we provide a specific character then it splits with that specific character.
*/



let str="helloworld!";

console.log(str.split());
console.log(str.split(''));
console.log(str.split('l'));


let demo="Ab*c*dke*doe*dep*e";


console.log(demo.split('*'));






